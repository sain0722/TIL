package com.example.bridge01;

public class UserInfo {

    public String id;
    public String pw;
    public String name;
    public String hobby;
    public String phone;

    public UserInfo(String id, String pw, String name, String hobby, String phone) {
        this.id = id;
        this.pw = pw;

        this.name = name;
        this.hobby = hobby;
        this.phone = phone;
    }
}
